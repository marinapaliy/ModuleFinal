import java.util.Random;

public class Main {
    public static void main(String[] args) {
        // Розмір масиву
        int rows = 5;     // Кількість рядків в двовимірному масиві
        int columns = 8;  // Кількість стовпців в двовимірному масиві

        // Створення двовимірного масиву
        int[][] array = new int[rows][columns];  // Створення масиву розміром 5x8

        // Заповнення масиву випадковими числами з відрізка [-99;99]
        Random random = new Random();  // Створення об'єкту для генерації випадкових чисел
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < columns; j++) {
                // Генерування випадкового числа від -99 до 99 та запис у масив
                array[i][j] = random.nextInt(199) - 99;
            }
        }

        // Виведення масиву на екран
        System.out.println("Двовимірний масив:");
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < columns; j++) {
                // Виведення кожного елемента масиву, розділеного пробілом
                System.out.print(array[i][j] + " ");
            }
            System.out.println();  // Перехід на новий рядок для наступного рядка масиву
        }

        // Пошук максимального значення в масиві
        int max = array[0][0];  // Початкове значення максимуму - перший елемент масиву
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < columns; j++) {
                if (array[i][j] > max) {
                    // Якщо поточний елемент більший за максимум, оновити максимум
                    max = array[i][j];
                }
            }
        }

        // Виведення максимального значення на екран
        System.out.println("Максимальний елемент в масиві: " + max);
    }
}
